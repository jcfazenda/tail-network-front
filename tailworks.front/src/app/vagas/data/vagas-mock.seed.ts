function seedJob(
  job: Omit<MockJobRecord, 'createdAt' | 'updatedAt' | 'hiringDocuments' | 'talentSubmittedDocuments' | 'talentDocumentsConsentAccepted'>
    & Partial<Pick<MockJobRecord, 'createdAt' | 'updatedAt' | 'hiringDocuments' | 'talentSubmittedDocuments' | 'talentDocumentsConsentAccepted'>>,
): MockJobRecord {
  return {
    ...job,
    hiringDocuments: [...(job.hiringDocuments ?? [])],
    experienceStacks: (job.experienceStacks ?? []).map((item) => ({ ...item })),
    talentSubmittedDocuments: [...(job.talentSubmittedDocuments ?? [])],
    talentDocumentsConsentAccepted: job.talentDocumentsConsentAccepted ?? false,
    createdAt: job.createdAt ?? now,
    updatedAt: job.updatedAt ?? job.createdAt ?? now,
  };
}

function benefit(title: string, sideLabel?: string, description?: string): JobBenefitItem {
  return { title, sideLabel, description };
}

function benefitList(...items: Array<string | JobBenefitItem>): JobBenefitItem[] {
  return items.map((item) => (typeof item === 'string' ? benefit(item) : item));
}

function responsibilitySections(...items: string[]): JobResponsibilitySection[] {
  return [
    {
      id: 'summary-section-1',
      pageId: 'front',
      title: 'Requisitos e habilidades que buscamos:',
      items: [...items],
    },
  ];
}

